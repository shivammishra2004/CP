std::vector<int> findDivisors(int n) {
    std::vector<int> divisors;
    for (int i = 1; i <= std::sqrt(n); i++) {
        if (n % i == 0) {
            if (i != n) {
                divisors.push_back(i);
            }
            if (i != n / i && n / i != n) {
                divisors.push_back(n / i);
            }
        }
    }
    return divisors;
}

